"use client"

import { symptoms as allSymptoms } from "@/lib/data/symptoms"
import Link from "next/link"
import {
  AlertTriangle,
  ArrowLeft,
  Flower2,
  Leaf,
  ShieldAlert,
  TrendingUp,
  Info,
} from "lucide-react"
import { cn } from "@/lib/utils"

interface PredictionMedicine {
  id: string
  localName: string
  scientificName: string
  englishName: string
  plantType: string
  partUsed: string
  preparationMethod: string
  traditionalUsage: string
  safetyNotes: string
  region: string
}

interface Prediction {
  disease: {
    id: string
    name: string
    description: string
    severity: "mild" | "moderate" | "severe"
    commonAge: string
    explanation: string
  }
  confidence: number
  matchedSymptoms: string[]
  totalSymptoms: number
  topInfluencingSymptoms: { symptomId: string; weight: number }[]
  medicines: PredictionMedicine[]
}

interface PredictionResultsProps {
  symptomIds: string[]
  predictions: Prediction[]
  selectedSymptomCount: number
}

export function PredictionResults({ symptomIds, predictions, selectedSymptomCount }: PredictionResultsProps) {
  const symptomNameMap = new Map(allSymptoms.map((s) => [s.id, s.name]))

  if (predictions.length === 0) {
    return (
      <div className="mx-auto max-w-lg rounded-xl border p-8 text-center">
        <Info className="mx-auto h-8 w-8 text-muted-foreground" />
        <h2 className="mt-3 text-lg font-semibold text-foreground">
          No Strong Matches Found
        </h2>
        <p className="mt-2 text-sm text-muted-foreground">
          Your symptom combination did not closely match any conditions in our database.
          Consider selecting different or additional symptoms.
        </p>
        <Link
          href="/symptom-checker"
          className="mt-4 inline-flex items-center gap-2 rounded-lg bg-primary px-4 py-2 text-sm font-semibold text-primary-foreground"
        >
          <ArrowLeft className="h-4 w-4" />
          Adjust Symptoms
        </Link>
      </div>
    )
  }

  return (
    <div className="flex flex-col gap-8">
      {/* Disclaimer */}
      <div className="flex items-start gap-3 rounded-lg border border-destructive/20 bg-destructive/5 p-4">
        <ShieldAlert className="mt-0.5 h-5 w-5 shrink-0 text-destructive" />
        <div>
          <p className="text-sm font-semibold text-destructive">
            Important Medical Disclaimer
          </p>
          <p className="mt-1 text-xs leading-relaxed text-destructive/80">
            These results are generated by a symptom-matching algorithm for educational purposes
            only. They are NOT a medical diagnosis. Always consult a qualified healthcare
            professional for proper diagnosis and treatment. If symptoms are severe, seek
            emergency medical care immediately.
          </p>
        </div>
      </div>

      {/* Summary */}
      <div className="rounded-xl border bg-card p-5">
        <h2 className="text-sm font-semibold text-muted-foreground">
          Analysis Summary
        </h2>
        <p className="mt-1 text-sm text-foreground">
          Based on <strong>{selectedSymptomCount} symptoms</strong>, we found{" "}
          <strong>{predictions.length} possible condition{predictions.length > 1 ? "s" : ""}</strong>.
        </p>
        <div className="mt-3 flex flex-wrap gap-1.5">
          {symptomIds.map((id) => (
            <span
              key={id}
              className="rounded-full bg-primary/10 px-2.5 py-1 text-xs font-medium text-primary"
            >
              {symptomNameMap.get(id) || id}
            </span>
          ))}
        </div>
      </div>

      {/* Prediction cards */}
      {predictions.map((prediction, index) => (
        <div key={prediction.disease.id} className="rounded-xl border bg-card overflow-hidden">
          {/* Header */}
          <div className="flex items-center justify-between border-b bg-muted/30 px-5 py-4">
            <div className="flex items-center gap-3">
              <span className="flex h-8 w-8 items-center justify-center rounded-full bg-primary/10 text-sm font-bold text-primary">
                {index + 1}
              </span>
              <div>
                <h3 className="font-serif text-xl font-bold text-foreground">
                  {prediction.disease.name}
                </h3>
                <p className="text-sm text-muted-foreground">
                  {prediction.disease.description}
                </p>
              </div>
            </div>
            <span
              className={cn(
                "rounded-full px-2.5 py-1 text-xs font-semibold",
                prediction.disease.severity === "mild" &&
                  "bg-chart-2/10 text-chart-2",
                prediction.disease.severity === "moderate" &&
                  "bg-accent/10 text-accent-foreground",
                prediction.disease.severity === "severe" &&
                  "bg-destructive/10 text-destructive"
              )}
            >
              {prediction.disease.severity.charAt(0).toUpperCase() +
                prediction.disease.severity.slice(1)}
            </span>
          </div>

          <div className="p-5">
            {/* Confidence */}
            <div>
              <div className="flex items-center justify-between">
                <span className="text-sm font-medium text-muted-foreground">
                  Confidence Score
                </span>
                <span className="text-lg font-bold text-foreground">
                  {prediction.confidence}%
                </span>
              </div>
              <div className="mt-2 h-3 rounded-full bg-muted">
                <div
                  className={cn(
                    "h-3 rounded-full transition-all",
                    prediction.confidence >= 60
                      ? "bg-primary"
                      : prediction.confidence >= 40
                        ? "bg-accent"
                        : "bg-muted-foreground"
                  )}
                  style={{ width: `${prediction.confidence}%` }}
                />
              </div>
              <p className="mt-1.5 text-xs text-muted-foreground">
                {prediction.matchedSymptoms.length} of {prediction.totalSymptoms} known
                symptoms matched
              </p>
            </div>

            {/* Explanation */}
            <div className="mt-5 rounded-lg bg-muted/30 p-4">
              <p className="text-sm leading-relaxed text-foreground">
                {prediction.disease.explanation}
              </p>
              <p className="mt-2 text-xs text-muted-foreground">
                Common age group: {prediction.disease.commonAge}
              </p>
            </div>

            {/* Top Influencing Symptoms */}
            <div className="mt-5">
              <div className="flex items-center gap-2">
                <TrendingUp className="h-4 w-4 text-primary" />
                <h4 className="text-sm font-semibold text-foreground">
                  Top Influencing Symptoms
                </h4>
              </div>
              <div className="mt-2 flex flex-col gap-1.5">
                {prediction.topInfluencingSymptoms.map((s) => (
                  <div key={s.symptomId} className="flex items-center gap-3">
                    <span className="w-28 truncate text-sm text-foreground">
                      {symptomNameMap.get(s.symptomId) || s.symptomId}
                    </span>
                    <div className="flex-1">
                      <div className="h-1.5 rounded-full bg-muted">
                        <div
                          className="h-1.5 rounded-full bg-primary/60"
                          style={{ width: `${s.weight * 100}%` }}
                        />
                      </div>
                    </div>
                    <span className="w-10 text-right text-xs text-muted-foreground">
                      {Math.round(s.weight * 100)}%
                    </span>
                  </div>
                ))}
              </div>
            </div>

            {/* Traditional Medicines */}
            {prediction.medicines.length > 0 && (
              <div className="mt-6">
                <div className="flex items-center gap-2">
                  <Flower2 className="h-4 w-4 text-primary" />
                  <h4 className="text-sm font-semibold text-foreground">
                    Ethiopian Traditional Remedies
                  </h4>
                </div>
                <div className="mt-3 grid gap-3 sm:grid-cols-2">
                  {prediction.medicines.map((medicine) => (
                    <div
                      key={medicine.id}
                      className="rounded-lg border p-4"
                    >
                      <div className="flex items-start gap-3">
                        <div className="flex h-9 w-9 shrink-0 items-center justify-center rounded-lg bg-primary/10">
                          <Leaf className="h-4 w-4 text-primary" />
                        </div>
                        <div className="min-w-0">
                          <p className="font-semibold text-foreground">
                            {medicine.localName}
                          </p>
                          <p className="text-xs italic text-muted-foreground">
                            {medicine.scientificName}
                          </p>
                          <p className="text-xs text-muted-foreground">
                            {medicine.englishName} &middot; {medicine.plantType}
                          </p>
                        </div>
                      </div>
                      <div className="mt-3 flex flex-col gap-2 text-xs">
                        <div>
                          <span className="font-semibold text-foreground">
                            Part used:{" "}
                          </span>
                          <span className="text-muted-foreground">
                            {medicine.partUsed}
                          </span>
                        </div>
                        <div>
                          <span className="font-semibold text-foreground">
                            Preparation:{" "}
                          </span>
                          <span className="text-muted-foreground">
                            {medicine.preparationMethod}
                          </span>
                        </div>
                        <div className="rounded-md bg-accent/10 p-2">
                          <span className="font-semibold text-accent-foreground">
                            Safety:{" "}
                          </span>
                          <span className="text-accent-foreground/80">
                            {medicine.safetyNotes}
                          </span>
                        </div>
                      </div>
                    </div>
                  ))}
                </div>
              </div>
            )}
          </div>
        </div>
      ))}

      {/* Back */}
      <div className="flex justify-center">
        <Link
          href="/symptom-checker"
          className="inline-flex items-center gap-2 rounded-lg border bg-card px-5 py-2.5 text-sm font-semibold text-foreground transition-colors hover:bg-muted"
        >
          <ArrowLeft className="h-4 w-4" />
          Check Different Symptoms
        </Link>
      </div>
    </div>
  )
}
